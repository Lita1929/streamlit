import joblib

# Load the Random Forest model
model = joblib.load("model/model_rf.joblib")

# Map model output to original labels
label_map = {
    0: "dropout",
    1: "enrolled",
    2: "graduate"
}

def prediction(data):
    """
    Make prediction from preprocessed data.

    Args:
        data (pandas.DataFrame): Preprocessed input data

    Returns:
        str: Predicted status label ('dropout', 'enrolled', or 'graduate')
    """
    result = model.predict(data)
    return label_map[result[0]]

